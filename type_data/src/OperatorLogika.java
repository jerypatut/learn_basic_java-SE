public class OperatorLogika {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        // AND (&&)
        System.out.println("a && b: " + (a && b)); // false

        // OR (||)
        System.out.println("a || b: " + (a || b)); // true

        // NOT (!)
        System.out.println("!a: " + (!a)); // false
    }
}
