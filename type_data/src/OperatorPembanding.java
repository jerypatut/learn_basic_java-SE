public class OperatorPembanding {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Pembandingan menggunakan operator pembanding
        System.out.println("a == b: " + (a == b)); // false
        System.out.println("a != b: " + (a != b)); // true
        System.out.println("a > b: " + (a > b)); // false
        System.out.println("a < b: " + (a < b)); // true
        System.out.println("a >= b: " + (a >= b)); // false
        System.out.println("a <= b: " + (a <= b)); // true
    }
}
