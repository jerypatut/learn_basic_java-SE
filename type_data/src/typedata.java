public class typedata {
    public static void main(String[] args) {

        int angka = 12;
        double angkaPecahan = 12.5;
        char karakter = 'A';
        String kalimat = "hallo selamat siang semuanya";
        boolean kondisi = true;

        /// tampilkan semua hasil dari type data diatas

        System.out.println("Angka : " + angka + " THIS IS TYPE DATA NUMBER");
        System.out.println("Angka Pecahan : " + angkaPecahan + "  THIS IS TYPEDATA DOUBLE");
        System.out.println("Karakter : " + karakter + " THIS IS TYPE DATA CHAR");
        System.out.println("Kalimat : " + kalimat + " THIS IS TYPE DATA STRING");
        System.out.println("Kondisi : " + kondisi + " THIS IS KONDISI TRUE OR FALSE");

    }
}
